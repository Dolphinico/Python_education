#Работа со списками при помощи циклов
wizards = ["lupa", "pupa", "goga"]
for wizard in wizards: # цикл for определяет объекты из указанного списка и проводит с ними операции до тех пор, пока спиок не закончится
    print(wizard.title() + ", that was a great trick!") 
    print(wizard.title() + ", do some more tricks" + "!\n")
print("Thank you everyone!")

