#Функция Range() для создания числовой последовательности

for value in range(1,11): #тут также происходит смещение вместо 1-11 выводится 1-10, потому что отчет идет с 0
	print(value)

numbers = list(range(1,11)) #создание числового списка через метод list() путем добавления в него функции range()
print(numbers)

#также при помощи range() можно генерить числовые последоватальности пропуская числа в указаном диапазоне
even_numbers = list(range(2,11,2)) #(2 <- число с которого начнется список, 11 <- общее количство чисел, 2<- число к которое будет прибавляться к первому)
