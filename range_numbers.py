#диапазон чисел на примере списка квадратов

squares = []
for value in range(1,11):
	square = value**2
	squares.append(square)
print(squares)

#более простой вариант

squares = []
for value in range(1,11):
	squares.append(value**2) #после возведения первого числа в степень, append добавляет следующее число в списке и также сразу возводит его в степень и тд
print(squares)

#простая статистика с числовыми числами

digits = [1,2,3,4,5,6,7,8,9,0] 
a = min(digits) # минимальное число в списке
b = max(digits) # максимальное число в списке
c = sum(digits) # сумма всех числе в списке
print(a,b,c)

